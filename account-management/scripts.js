saveEdit.addEventListener('click', async e => {
    e.preventDefault();

    const userName = document.getElementById("edit-user-name").value.trim();
    const email = document.getElementById("edit-email").value.trim();

    errorEditName.classList = "hidden";
    errorEditEmail.classList = "hidden";
    errorEditPass.classList = "hidden";

    if(userName != ""){
        if(email != ""){
            if(editpassword != ""){
                if(email.includes("@gmail.com")){
                    const accounts = await getTasksFromDB();
                    let emailExist = false
                    let id;
                    accounts.forEach(acc =>{
                        if(acc.email === email && acc.email != sessionStorage.getItem(ACCOUNTEMAIL)){
                            emailExist = true;
                        }
                        if(acc.email === sessionStorage.getItem(ACCOUNTEMAIL)){
                            id = acc.id;
                        }
                    });
                    if(emailExist){
                        errorEditEmail.innerHTML = EMAILEXISITNG;
                        errorEditEmail.classList.toggle("hidden");
                    } else {
                        await updateTaskInDB(id, userName, email, editpassword);

                        sessionStorage.setItem(ACCOUNTNAME, userName);
                        sessionStorage.setItem(ACCOUNTEMAIL, email);
                        sessionStorage.setItem(ACCOUNTPASS, editpassword);

                        cancelEdit.click();
                    }
                } else{
                    errorEditEmail.innerHTML = INCORRECTEMAIL;
                    errorEditEmail.classList.toggle("hidden");
                } 
            } else {
                errorEditPass.innerHTML = EMPTYFIELD;
                errorEditPass.classList.toggle("hidden");
            }
        } else {
            errorEditEmail.innerHTML = EMPTYFIELD;
            errorEditEmail.classList.toggle("hidden");
        }
    } else {
        errorEditName.innerHTML = EMPTYFIELD;
        errorEditName.classList.toggle("hidden"); 
    }
});

retrieveAccBtn.addEventListener('click', async e => {
    e.preventDefault();
    
    const email = document.getElementById("login-email").value.trim();

    errorLogEmail.classList = "hidden";
    errorLogPass.classList = "hidden";

    if(email != ""){
        if(loginpassword != ""){
            if(email.includes("@gmail.com")){
                const accounts = await getTasksFromDB();
                let emailExist = false
                let passCorrect = false
                accounts.forEach(acc =>{
                    if(acc.email === email){
                        emailExist = true;
                        if(acc.password === loginpassword){
                            sessionStorage.setItem(USERLOGGEDIN, true);
                            sessionStorage.setItem(ACCOUNTNAME, acc.userName);
                            sessionStorage.setItem(ACCOUNTEMAIL, acc.email);
                            sessionStorage.setItem(ACCOUNTPASS, acc.password);
                            passCorrect = true;
                            logInputFields.reset(); 
                        }
                    }
                });
                if(!emailExist){
                    errorLogEmail.innerHTML = NULLEMAIL;
                    errorLogEmail.classList.toggle("hidden");
                } else if(!passCorrect){
                    errorLogPass.innerHTML = WRONGPASS;
                    errorLogPass.classList.toggle("hidden");
                } else {
                    accContainer.style.display = "block";
                    logContainer.style.display = "none";
                    accName.innerHTML = sessionStorage.getItem(ACCOUNTNAME);
                    accEmail.innerHTML = sessionStorage.getItem(ACCOUNTEMAIL);
                    accPassword.innerHTML = sessionStorage.getItem(ACCOUNTPASS);
                }
            } else{
                errorLogEmail.innerHTML = INCORRECTEMAIL;
                errorLogEmail.classList.toggle("hidden");
            } 
        } else {
            errorLogPass.innerHTML = EMPTYFIELD;
            errorLogPass.classList.toggle("hidden");
        }
    } else {
        errorLogEmail.innerHTML = EMPTYFIELD;
        errorLogEmail.classList.toggle("hidden");
    }
   
});

loginPass.addEventListener('keydown', e => {
    setTimeout(() => {
        let passLength = loginpassword.length;
        if(loginPass.value.length < loginpassword.length){
            loginpassword = loginpassword.substring(0, loginPass.value.length)
        } else if(loginPass.value.length != loginpassword.length || loginpassword === "") {
            loginpassword += loginPass.value.trim().charAt(loginPass.value.length - 1);
        }    
        if(e.code != "Backspace" && !showingLogPass){
            passLength = loginpassword.length;
            loginPass.value = "*".repeat(passLength);
        }
        // console.log(password);
        // console.log(loginPass.value);
    }, 0); 
});

